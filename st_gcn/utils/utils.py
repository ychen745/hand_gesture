import torch
import os
from utils.graph import Graph
import logging

def save_model(model, model_path, epoch, optimizer, precision):
    try:
      # remove 'module' prefix of data parallel model
      state_dict = model.module.state_dict()
    except AttributeError:
      state_dict = model.state_dict()  

    state = {
        'epoch' : epoch,
        'precision' : precision,
        'state_dict' : state_dict,
        'optimizer' : optimizer.state_dict()
    }
    torch.save(state, model_path)
    print('The model has been saved as {}.'.format(model_path))
    logging.info('The model has been saved as {}.'.format(model_path))

def load_model(model_path, model, device):
    print("model path:", model_path, "device:", device)
    logging.info('model path: %s, device: %s' %(model_path, device))
    assert os.path.exists(model_path)
    checkpoint = torch.load(model_path, map_location = device)
    state_dict = checkpoint['state_dict']
    for k, v in model.state_dict().items():
        if k not in list(state_dict):
            logging.info('key "{}" could not be found in provided state dict'.format(k))
        elif state_dict[k].shape != v.shape:
            logging.info('key "{}" is of different shape in model and provided state dict'.format(k))
            state_dict[k] = v

    model.load_state_dict(checkpoint['state_dict'], strict=False)
    print('Loaded model from {}.'.format(model_path))
    logging.info('Loaded model from {}.'.format(model_path))

def load_pretrained_model(model_path, model, ignored_weights, device):
    assert os.path.exists(model_path)
    checkpoint = torch.load(model_path, map_location = device)
    pretrained_dict = checkpoint['state_dict']
    model_dict = model.state_dict()
    pretrained_dict = {k: v for k, v in pretrained_dict.items() if k in model_dict and k not in ignored_weights}
    model_dict.update(pretrained_dict)
    model.load_state_dict(model_dict)
    print('Loaded pretrained mode from {}.'.format(model_path))
    logging.info('Loaded pretrained mode from {}.'.format(model_path))
    print('ignored weights')
    logging.info('ignored weights')
    print(ignored_weights)
    logging.info(ignored_weights)

